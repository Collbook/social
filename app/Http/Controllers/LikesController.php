    public function like($id)
    {
        $reply = Reply::find($id);
        Like::create([
            'reply_id' => $id,
            'user_id'  => Auth::id(),
        ]);
        Toastr::success('You has like the reply','Success');
        return redirect()->back();
    }


    public function unlike($id)
    {
        $like = Like::where([
            ['reply_id', '=', $id],
            ['user_id', '=', Auth::id()],
        ])->first();

        $like->delete();

        Toastr::success('You has unlike the reply','Success');
        return redirect()->back();

    }
