    public function best_answer($id)
    {
        //return $id;
        $reply = Reply::find($id);
        $reply->best_answer = $reply->best_answer + 1;
        $reply->save();

        // point 
        $reply->user->point += 50;
        $reply->user->save();

        Toastr::success('Mark as best answer successfully','Success');
        return redirect()->back();
    }

